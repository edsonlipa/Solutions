#include<bits/stdc++.h>
using namespace std;

int main(int argc, char const *argv[]) {
  std::cout << "1" << '\n';
  std::cout << "1000 1" << '\n';
  for (int i = -pow(10,9); i < pow(10,9); i=i+2000000) {
    std::cout << i << ' ';
  } std::cout << '\n';
  std::cout << "1 1000" << '\n';

  return 0;
}
